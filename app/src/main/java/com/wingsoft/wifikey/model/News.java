package com.wingsoft.wifikey.model;

public class News {
	private String mTitle;
	private String mPublishTime;
	private String mId;
	private String mFirstPicUrl;

	public String getmTitle() {
		return mTitle;
	}

	public void setmTitle(String mTitle) {
		this.mTitle = mTitle;
	}

	public String getmPublishTime() {
		return mPublishTime;
	}

	public void setmPublishTime(String mPublishTime) {
		this.mPublishTime = mPublishTime;
	}

	public String getmId() {
		return mId;
	}

	public void setmId(String mId) {
		this.mId = mId;
	}

	public String getmFirstPicUrl() {
		return mFirstPicUrl;
	}

	public void setmFirstPicUrl(String mFirstPicUrl) {
		this.mFirstPicUrl = mFirstPicUrl;
	}
}
